-----------------------------
-- Tablas Principales
-----------------------------

CREATE TABLE FINANZAS_APP.USUARIOS (
    id             NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    supabase_uid   VARCHAR2(255) UNIQUE NOT NULL,
    email          VARCHAR2(255) NOT NULL,
    fecha_creacion DATE DEFAULT SYSDATE
);

CREATE TABLE FINANZAS_APP.CATEGORIAS (
    id     NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    nombre VARCHAR2(100) NOT NULL,
    tipo   VARCHAR2(20) CHECK (tipo IN ('INGRESO', 'GASTO')) NOT NULL
);

CREATE TABLE FINANZAS_APP.TRANSACCIONES (
    id              NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    usuario_id      NUMBER NOT NULL,
    categoria_id    NUMBER NOT NULL,
    monto           NUMBER(15, 2) NOT NULL,
    fecha           DATE DEFAULT SYSDATE,
    descripcion     VARCHAR2(255),
    comprobante_url VARCHAR2(500),
    CONSTRAINT fk_trans_usuario FOREIGN KEY (usuario_id) REFERENCES FINANZAS_APP.USUARIOS(id),
    CONSTRAINT fk_trans_categoria FOREIGN KEY (categoria_id) REFERENCES FINANZAS_APP.CATEGORIAS(id)
);

