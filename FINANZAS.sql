-----------------------------
-- Procedimientos y Lógica
-----------------------------

CREATE OR REPLACE PROCEDURE FINANZAS_APP.sincronizar_usuario (
    p_supabase_uid IN VARCHAR2,
    p_email        IN VARCHAR2
) AS
BEGIN
    INSERT INTO FINANZAS_APP.USUARIOS (supabase_uid, email)
    SELECT p_supabase_uid, p_email FROM DUAL
    WHERE NOT EXISTS (
        SELECT 1 FROM FINANZAS_APP.USUARIOS WHERE supabase_uid = p_supabase_uid
    );
    COMMIT;
END;
