-----------------------------
-- API REST (ORDS)
-----------------------------

BEGIN
  ORDS.DEFINE_MODULE(
      p_module_name     => 'api_finanzas',
      p_base_path       => '/api/',
      p_items_per_page  => 100,
      p_status          => 'PUBLISHED'
  );

  -- Endpoint: Usuarios
  ORDS.DEFINE_TEMPLATE(
      p_module_name => 'api_finanzas',
      p_pattern     => 'usuarios/registro'
  );

  ORDS.DEFINE_HANDLER(
      p_module_name => 'api_finanzas',
      p_pattern     => 'usuarios/registro',
      p_method      => 'POST',
      p_source_type => ORDS.source_type_plsql,
      p_source      => 'BEGIN FINANZAS_APP.sincronizar_usuario(:uid, :email); END;'
  );

  -- Endpoint: Movimientos por Usuario
  ORDS.DEFINE_TEMPLATE(
      p_module_name => 'api_finanzas',
      p_pattern     => 'movimientos/:uid'
  );

  ORDS.DEFINE_HANDLER(
      p_module_name => 'api_finanzas',
      p_pattern     => 'movimientos/:uid',
      p_method      => 'GET',
      p_source_type => ORDS.source_type_collection_feed,
      p_source      => 'SELECT t.id, t.monto, t.fecha, t.descripcion, c.nombre AS categoria_nombre, c.tipo AS tipo_movimiento, t.comprobante_url 
                        FROM FINANZAS_APP.TRANSACCIONES t, FINANZAS_APP.CATEGORIAS c, FINANZAS_APP.USUARIOS u 
                        WHERE t.categoria_id = c.id 
                          AND t.usuario_id = u.id 
                          AND u.supabase_uid = :uid 
                        ORDER BY t.fecha DESC'
  );

  -- Endpoint: Crear Movimiento
  ORDS.DEFINE_TEMPLATE(
      p_module_name => 'api_finanzas',
      p_pattern     => 'movimientos'
  );

  ORDS.DEFINE_HANDLER(
      p_module_name => 'api_finanzas',
      p_pattern     => 'movimientos',
      p_method      => 'POST',
      p_source_type => ORDS.source_type_plsql,
      p_source      => '
      DECLARE
        v_usuario_id NUMBER;
      BEGIN
        SELECT id INTO v_usuario_id FROM FINANZAS_APP.USUARIOS WHERE supabase_uid = :uid;
        INSERT INTO FINANZAS_APP.TRANSACCIONES (usuario_id, categoria_id, monto, descripcion)
        VALUES (v_usuario_id, :categoria_id, :monto, :descripcion);
        COMMIT;
      END;'
  );

  -- Endpoint: Acciones sobre Movimiento Específico (Eliminar y Editar)
  ORDS.DEFINE_TEMPLATE(
      p_module_name => 'api_finanzas',
      p_pattern     => 'movimientos/:id'
  );

  ORDS.DEFINE_HANDLER(
      p_module_name => 'api_finanzas',
      p_pattern     => 'movimientos/:id',
      p_method      => 'DELETE',
      p_source_type => ORDS.source_type_plsql,
      p_source      => 'BEGIN DELETE FROM FINANZAS_APP.TRANSACCIONES WHERE id = :id; COMMIT; END;'
  );

  ORDS.DEFINE_HANDLER(
      p_module_name => 'api_finanzas',
      p_pattern     => 'movimientos/:id',
      p_method      => 'PUT',
      p_source_type => ORDS.source_type_plsql,
      p_source      => 'BEGIN 
                          UPDATE FINANZAS_APP.TRANSACCIONES 
                          SET categoria_id = :categoria_id, 
                              monto = :monto, 
                              descripcion = :descripcion 
                          WHERE id = :id; 
                          COMMIT; 
                        END;'
  );

  COMMIT;
END;
